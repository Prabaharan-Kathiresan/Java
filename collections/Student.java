public class Student {
    String name;
    String rollno;
    int total;
    String year;
    Char section;
    String fatherName;
    String motherName;

    Student(String name,String rollno,int total,String year,Char section,String fatherName,String motherName)  {
        this.name = name;
        this.rollno=rollno;
        this.total=total;
        this.year=year;
        this.section=section;
        this.fatherName=fatherName;
        this.motherName=motherName;
    }
}